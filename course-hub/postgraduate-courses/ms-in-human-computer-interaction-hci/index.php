                    <h3>US based entry requirements</h3>
                    <p>US based entry requirements -<strong> Students should have completed a four-year U.S. bachelor’s degree or the U.S. equivalent</strong>. Average GRE scores of admitted graduate students (all programs) are: 159 (verbal), 163 (quantitative), 4.1 (analytical). In addition to this, international students should have obtained a minimum TOEFL score of 88 on iBT/230 on CBT/570 on PBT. In lieu of the TOEFL student might have obtained a minimum IELTS score of 6.5, or PTE (Pearson’s Test of English) score of 60.</p>
<p>&nbsp;</p>
                    

                    
                    <h3>International entry requirements</h3>
                    <p>Unless otherwise specified, for any master’s degree, the student must earn at least 30 credits as a graduate student at T4U. No more than 9 of the 30 credits (earned with a grade of A, A-, B+, or B) may be transferred from institutions approved for this purpose by the Dean of the Graduate School. At least half of the required credits (not counting 6971) must be in the major.</p>
                    

