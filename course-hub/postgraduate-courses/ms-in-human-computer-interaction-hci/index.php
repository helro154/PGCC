                <a href="#schedule">Course Schedule</a>
                <div id="schedule" class="content">

                    
                    <p><strong>Computer Systems</strong></p>
<p>This module is intended to provide a basic understanding of the operation of the hardware components of computer systems and networks, enabling students to evaluate hardware and perform dimensioning estimations. The role of the operating system is also covered, relating it to the facilities needed by users, administrators and programmers.</p>
<p><strong>Computational Intelligence</strong></p>
<p>This module will provide a theoretical and practical overview of computational intelligence. This inclues the most recent artificial intelligence methods and approaches such as evolutionary computation, artificial neural networks, fuzzy logic, artificial life and cognitive robotics.</p>
                    

                    
                    <p><strong>Databases</strong></p>
<p>To introduce students to the concepts and issues concerning multiuser, networked relational databases in a practical environment. To provide a solid foundation in SQL.</p>
                    
                    
                    <p><strong>Information Retrieval</strong></p>
<p>This module considers the organisation, storage and retrieval of unstructured information based on its content. World Wide Web search engines will be used extensively both as a source of material and for demonstration purposes. Additionally, current issues in the information industry are addressed.</p>
                    


                    <p>Some highlights of this program include:</p>
<ul>
<li>An interdisciplinary orientation</li>
<li>A 12-month, 12-course duration</li>
<li>Diverse faculty from computer science, design, psychology, learning sciences, business administration, electrical engineering and human-computer interaction</li>
<li>A substantial project component for an external client, with a team orientation</li>
</ul>
