                <div id="schedule" class="content">
                    <h2>HIST 102 The Ancient World (3)</h2>
<p>This course explores the emergence and development of civilization in Asia and the Mediterranean world from the first appearance of ities around 3000 B.C. to the triumph of Christianity in the Roman Empire in the fourth century A.D. We will examine how ancient ideas, empires, social structures, art, literature, and religious beliefs emerged in response to the challenges that confronted ancient people as their world expanded and changed. Topics include empire, spirituality, gender roles, barbarians, slavery, democracy, warfare, diplomacy, and inter-regional trade and contact.</p>
<h2>HIST 103 The Medieval World (3)</h2>
<p>This course explores the tensions and transformations in European society between A.D. 300 and 1500, as well as points of contact between medieval societies within Europe itself, across the Mediterranean, and beyond. Topics include the Fall of the Roman Empire, Byzantium, the rise of Islam, Vikings, Mongols, social crisis and disorder, plague, the Norman Conquest of England, the Crusades, troubadours, saints, the medieval Papacy, medieval Christianity and its heresies, monasticism, the revival of classical learning, and voyages of exploration and discovery.</p>
<h2>HIST 116 War and Peace in the Modern World (3)</h2>
<p>The ending of the Cold War seemed to promise a new world order characterized by respect for human rights, principles of democracy, and the rule of law. Instead, we enter the 21st century plagued by global conflict and burdened by spasms of terrorism, radical nationalism, ethnic cleansing, a growing gap between rich and poor, and the proliferation of nuclear and biological weapons. Where did these problems arise and why have they not gone away? Furthermore, how have societies gone about managing conflict and sustaining peace over the past two hundred years or so? This class will assist students in gaining historical perspective on thesequestions by exploring the underlying causes of war, revolution, terrorism, and genocide in modern world history. The course will begin with an analysis of the contemporary scene and then back up to explore the historical evolution of conflict and its resolution since the era of revolutionary France. Utilizing a global perspective, students will analyze the strengths and weaknesses of various attempts at managing and resolving conflict in the modern world. (Meets lower-division requirement for the Peace and Justice Studies minor)</p>
<h2>HIST 118 U.S. History , 1877 to the present (3)</h2>
<p>This course is designed to explore America’s historical development from the Reconstruction era to the present. It explores a wide variety of factors (political, economic, social, and cultural) that contributed to the creation of a multicultural industrial society and that shaped America’s emergence as a world power. We will analyze key issues such as the changing relationships between government and the governed; the growth of a strong central state; the creation of a modern industrial economy; the evolution of an increasingly heterogeneous society; the country’s development into a world power; the Cold War at home and abroad; and the origins and consequences of the Vietnam War.</p>
<h2>HIST 126D American Women in History (3)</h2>
<p>This course explores the impact of historical events on the lives of American women and the varied roles women played in the shaping of American history. Topics include: witchcraft in New England; gender and family life under slavery; the impact of industrialization on women of different classes; the ideology of separate spheres; women’s political activities including the antislavery movement, the suffrage movement, the 19th Amendment, and the resurgence of feminism in the 1960s; and transformations in the lives of modern women including work, politics, sexuality, consumption patterns, and leisure activities.</p>
<h2>HIST 170 World History I (3)</h2>
<p>This course focuses on major themes in the history of humanity from 100,000 B.C. to A.D. 1500. It considers the evolution of the human species, the formation of huntergatherer societies, and the rise of great civilizations. It looks at how authority was manifested in architecture, government, writing, religion, philosophy, arts, science, and technology. A comparative approach will illuminate how world cultures differ, what they share, how they are differentiated, and what they exchange in the making of the modern world. The emphasis is on non-Western peoples.</p>
<h2>HIST 171 World History II (3)</h2>
<p>This course engages students in the study of modern world history in order to achieve a more critical and integrated understanding of global societies and cultures during the past five hundred years. Students will explore developments in Africa, Asia, the Americas, and Europe; consider the rise of the West after 1750; investigate the origins and outcomes of world war, revolution, and genocide in the 20th century; trace the disintegration of western empires after World War II; and ponder the global challenges of the post-Cold War era.</p>
<h2>HIST 240 Topics in Urban History (3)</h2>
<p>In this course, students study individual cities at unique moments in their historical development. Themes include the impact of the built environment on human experience, architecture as an expression of power, and the relationship between physical space and the development of community. Topics may include “Fin de Siècle Vienna” and “History of the American City,” among others.</p>
<h2>HIST 250 Topics in Comparative History (3)</h2>
<p>This course will offer a comparative perspective on a significant historical topic, which will assist students in clarifying what is and what is not unique to a particular historical experience. Special emphasis will be given to critiquing the notion of American “exceptionalism.” Topics may include “Comparative Frontiers,” “The Ghost Dance in Comparative Perspective,” “Comparative Imperialism,” and “Women under Communism.”</p>
<h2>HIST 260 Topics in the History of Science, Technology, and Medicine (3)</h2>
<p>This course will explore the various facets of the development of technology ranging from tool making among hunter-gatherers to the biotechnological revolution of the 21st century. Students will examine ongoing processes of human innovation and their impact on the individual and society. Topics may include “Science, Technology, and Medicine in the Pre-Modern Era,” “The Industrial Revolutions,” “History of the Brain,” and “The Biotechnological Revolution.”</p>
<h2>HIST 324 Christians , Muslims , and Jews in Medieval Spain (3)</h2>
<p>This course focuses on the society and culture of the pre-modern Iberian Peninsula with an emphasis on the conflict, coexistence, and diversity of interaction of its three religious groups: Christians, Jews, and Muslims. We will consider the territorial struggle between Christian and Muslim-ruled regimes over the course of many centuries, the environments of pre- and post-conquest societies and the frontier that separated them, and the ability of minority (and majority) religious groups to maintain themselves in these changing socio-religious contexts.</p>
<h2>HIST 334 European Art and Architecture in Context (3)</h2>
<p>This course explores the development of European art and architecture from 1600 to 1940. Students will “tour” some of Europe’s great architectural monuments, including Versailles, Kew Gardens, the Paris Opera House, and Vienna’s Secession Building. They will also look at corresponding trends in art, from the development of the Rococo to the triumph of Art Deco. Emphasis throughout will be on the personalities, political events, and social forces which shaped the development of European design.</p>
<h2>HIST 340 World War I (3)</h2>
<p>This course will examine the era of the Great War of 1900-1919. The origins of this global conflict included the decline of Pax Britannica in the 19th century, the rise of German nationalism, Balkan pan-slavism, and colonial rivalries. During this era, the old order dominated by European monarchies was swept aside by social revolutions, new ideologies, and a military conflict that cost 10 million lives. Modernism rose from the ashes of Victorian culture, and the new science transformed world thought.</p>
<h2>HIST 341 World War II (3)</h2>
<p>This course examines the origins of World War II, the economic and political challenges to interwar societies, the rise of the dictators, the experience of war and occupation, the holocaust, and the military struggle that led to millions of deaths and gave birth to the United Nations. Special topics include the Experience of Collaboration and Resistance in Europe, Civilians during World War II, the role of various professions, youth, and women during World War II.</p>
<h2>HIST 342 Birth of Two Nations : France and India (3)</h2>
<p>This course explores the birth of the modern nation state through the use of interactive role-playing games. Students “become” French revolutionaries inspired by Jean-Jacques Rousseau in “Rousseau, Burke, and Revolution in France, 1791.” They adopt the roles of Hindus and Muslims seeking to wrest political control away from the British Empire in “India on the Eve of Independence, 1945.” Students develop a deep understanding of nation building in France and India; they also explore how class conflict, religious divisions, and ethnic tensions contribute to the birth of nations.</p>
