                    <p>After completing the course attendees will be able to:</p>
<ul>
<li>Appreciate the wider engineering issues that form the background to developing complex and evolving software-intensive systems.</li>
<li>Plan and deliver an effective software engineering process, based on knowledge of widely used development lifecycle models.</li>
<li>Employ group working skills including general organization, planning and time management and inter-group negotiation.</li>
<li>Capture, document and analyse requirements.</li>
<li>Translate a requirements specification into an implementable design, following a structured and organised process.</li>
<li>Make effective use of UML, along with design strategies such as defining a software architecture, separation of concerns and design patterns.</li>
<li>Formulate a testing strategy for a software system, employing techniques such as unit testing, test driven development and functional testing.</li>
<li>Evaluate the quality of the requirements, analysis and design work done during the module.</li>
</ul>
