                <a href="#schedule">Course Schedule</a>
                <div id="schedule" class="content">
                    <p><span class="pme-courses">EE 382C Introduction to Software Engineering Processes - 3 credit hrs</span><br>This course is an introduction to software engineering with an emphasis on the methods, techniques and technology to build and evolve software systems. The emphasis is on software engineering principles.</p>
<p><span class="pme-courses">EE 379K Requirements Engineering: Acquisition and Modeling - 3 credit hrs</span><br>This course will address theoretical and practical methods for acquiring and modeling requirements for various systems stakeholders. Topics will include methods and techniques for managing the acquisition process among distributed team members and distributed stakeholders, eliciting and verifying requirements as a function of the type of stakeholder, the types of requirements, and system development maturity, managing the requirements artifacts, constructing model-based representations of requirements, synthesizing requirements for various stakeholders, and analyzing and evolving model-based requirements.</p>
<p><span class="pme-courses">EE 382C Software Architectures - 3 credit hrs</span><br>The course will teach students about software architectures, architectural model specification techniques and analysis techniques offered by the research community as well as those architectures, model specifications and analytical methods commonly used in industry.</p>
<p><span class="pme-courses">EE 382C.3 Verification and Validation - 3 credit hrs</span><br>This course covers various traditional and state-of-the-art techniques for software validation, a process that includes reasoning about (the correctness of) programs and testing programs. The course content will include both techniques for dynamic analysis, such as glass-box and black-box testing, equivalence partitioning, test strategy and automation, regression testing and debugging, and techniques for static analysis, such as symbolic execution, and also techniques for software model checking including those that employ artificial intelligence based heuristics.</p>
<p><span class="pme-courses">EE 382C Empirical Studies in Software Engineering - 3 credit hrs</span><br>Software Engineering is of necessity an empirical enterprise. This course looks at different kinds of empirical evaluation from related fields such as behavioral sciences to see how one can adapt those techniques to the service of software engineering.</p>
<p><span class="pme-courses">EE 382V: Formal Methods in Distributed Systems - 3 credit hrs</span><br>This course gives an introduction to the use of formal methods within the software design process. Specifically, this class will cover the application of models to distributed and concurrent systems. Modern software systems are commonly highly distributed, and this added sophistication further complicates software design. The rigor offered by formal methods aims to make the process more precise.</p>
<p><span class="pme-courses">EE 382V: Software Engineering Lifecycle - 3 credit hrs</span><br>This course addresses five major topics in the Software Engineering Lifecycle: requirements, design, construction, measurement &amp; evaluation, and maintenance &amp; evolution.</p>
<p><span class="pme-courses">EE 382V Advanced Programming Tools - 3 credit hrs</span><br>Programming is difficult - some of the problem developers face include.</p>
<ol>
<li>How can a project be structured so that developers can work on it concurrently?</li>
<li>How can the building of a project be automated?</li>
<li>How can a program be written to make it portable?</li>
<li>How can a program be prototyped efficiently?</li>
<li>How can a program be tested and debugged efficiently?</li>
<li>How can the performance of a program be increased?</li>
</ol>
<p>Using the right tools can solve these problems. Examples include tools for version control, documentation, program building and configuration, automatic testing, program analysis, and integrated development.</p>
<p>Our approach will be to introduce a specific problem, show how a tool can solve the problem, and then develop the technical principles underlying the tool. We will have written homework problems as well as coding exercises for each concept. The class will have a major design project that will begin at the start of the term. Use of the tools will be a required part of the project. We will use open-source tools to illustrate these concepts. The specific tool stack is described in the lectures section of this document. I selected these tools based on my experience at Google; they also power many state-of-the-art commercial projects.</p>
