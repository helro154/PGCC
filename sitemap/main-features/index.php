      <h1>Main Features</h1>

           <!-- end of SM header -->
<h2 class="h3">Sample Data is jam-packed with features to super charge your site. The listing below gives an overview of all the highlights.</h2>

<dl class="accordion" data-accordion>
  <dt class="hidden">Choose question</dt>
    <dd class="accordion-navigation">
    <a href="#item-4697">A-Z</a>
    <div id="item-4697" class="content">
      <p><a href="/a-z/">View&nbsp;A-Z</a></p>
<p>This listing is created using the built in Site Manager 'A to Z Navigation' navigation object. It shows an A-Z listing of all visible sections.</p>
    </div>
  </dd>  <dd class="accordion-navigation">
    <a href="#item-4708">TERMINALFOUR Blog</a>
    <div id="item-4708" class="content">
      <p><a href="/life-at-t4u/blog/">View blog</a></p>
<p>The T4 blog is similar to&nbsp;<a href="http://www.terminalfour.com/blog/" target="_blank">http://www.terminalfour.com/blog/</a>&nbsp;Uses json/javascript tagging&nbsp;fueled by keywords.&nbsp;</p>
    </div>
  </dd>  <dd class="accordion-navigation">
    <a href="#item-4701">Cookies - personalised journey</a>
    <div id="item-4701" class="content">
      <p><a href="/">View&nbsp;Cookies - personalised journey</a></p>
<p>Javascript cookies are created when a user chooses either Undergraduate or Postgraduate options in any of the course search blocks throughout the site. Once this cookie is in place the 'Quicklinks' content throughout the site will be change to be tailored for either Undergraduate or Postgraduate going forward.</p>
    </div>
  </dd>  <dd class="accordion-navigation">
    <a href="#item-4709">Course Catalog Builder</a>
    <div id="item-4709" class="content">
      <p><a href="/course-hub/course-catalog-builder/">View&nbsp;Course Catalog Builder</a></p>
<p>The course catalog is PHP-run application. It allow's Universities to upload their entire prospectus pdf into Site Manager and then give their users the ability to create a more customised version of that document, tailored to their specific interests. The appropriate sections of the document are outlined within Site Manager by page ranges spanning specific course information. The user simply chooses which information is relevant to them and then downloads a PDF with their tailored information.&nbsp;</p>
    </div>
  </dd>  <dd class="accordion-navigation">
    <a href="#item-4710">Course Compare</a>
    <div id="item-4710" class="content">
      <p><a href="/course-hub/course-compare/">View&nbsp;Course Compare</a></p>
<p>Course compare is a PHP module. Using javascript cookies it allows you to save courses to a saved course list as you browse or search courses. Once you have saved the courses you are interested in you can then choose to compare up to 3 courses at a time. The Site Manager admin user can choose from a list which elements should be displayed in the compare view. Courses can be added/removed from the saved course list as neccesary by the user.&nbsp;The compare page will list the information for up to 3 courses side by side to allow users to compare elements of each course.&nbsp;</p>
    </div>
  </dd>  <dd class="accordion-navigation">
    <a href="#item-4711">Course Hub - Advanced Course</a>
    <div id="item-4711" class="content">
      <p><a href="/course-hub/postgraduate-courses/ms-in-human-computer-interaction-hci/">View Course Hub - Advanced Course</a></p>
<p>The advanced setup allows for certain information to be stored in a central location and for it to be brought into any course as it is needed. For example you can enter your tutors elsewhere in your site structure and assign the course codes that they teach on. Any course that has the same course code as that tutor will show the information for that associated tutor. The content only needs to be updated and managed in one place for it to affect all courses with that tutor. If the information for a particular course is not suitable to use shared content then also have the ability to add content that is specific to that course only.</p>
<p>In this Sample Data example all Postgraduate courses are the 'Advanced Course' set up, while all undergraduate courses use the basic course content type.</p>
<p>In this example this content type is also hooked up to the <a href="/course-hub/course-compare/">course compare</a> feature to allow users to save and compare courses as they browse. Both course content types are also fed into the <a href="/course-hub/advanced-course-search/">course search feature</a>.</p>
    </div>
  </dd>  <dd class="accordion-navigation">
    <a href="#item-4695">Course Hub - Basic Course</a>
    <div id="item-4695" class="content">
      <p><a href="/course-hub/undergraduate-courses/marine-science/">View Course Hub - Basic Course</a></p>
<p>The basic course structure has all of your necessary course related elements within one course content template. Each piece of course content will be individually authored per course. In this Sample Data example all Undergraduate courses are 'Basic Course' set up, while all post-graduate courses use the more advanced course content type. In this example this content type is also hooked up to the <a href="/course-hub/course-compare/">course compare</a> feature to allow users to save and compare courses as they browse. Both course content types are also fed into the <a href="/course-hub/basic-course-search/">course search</a> feature.</p>
    </div>
  </dd>  <dd class="accordion-navigation">
    <a href="#item-4712">Course Search Advanced</a>
    <div id="item-4712" class="content">
      <p><a href="/course-hub/advanced-course-search/">View&nbsp;Course Search Advanced</a></p>
<p>'Course Search Advanced' is a PHP driven course search. It runs off JSON which is generated directly from the course content within Site Manager. The filters are multiple (for example you can filter by department AND duration AND campus at once). The ability to modify results are done via facets such as list facet, related terms facet, dropdown facets and a range facet. The searchable content is determined directly by the University by picking the preferred elements in the course content type to add to the JSON file. The course search has built in intelligent algorithms such as frequency, term order and proximity search criteria to make sure the results are relevant to the user’s search.</p>
<p>In this instance the advanced course search results have been hooked up to the <a href="/course-hub/course-compare/">course compare</a> feature to allow users to save and then compare courses.</p>
    </div>
  </dd>  <dd class="accordion-navigation">
    <a href="#item-4713">Course Search Basic</a>
    <div id="item-4713" class="content">
      <p><a href="/course-hub/basic-course-search/">View Course Search Basic</a></p>
<p>'Course Search Basic' is a PHP driven course search. It runs off JSON which is generated directly from the course content within Site Manager. The filters are singular and it is equipped with auto-suggest for keywords to make finding a course even easier.</p>
<p>In this instance the basic course result has been hooked up to the&nbsp;<a href="/course-hub/course-compare/">course compare</a>&nbsp;feature to allow users to save and then compare courses.</p>
    </div>
  </dd>  <dd class="accordion-navigation">
    <a href="#item-4716">Digital Signage</a>
    <div id="item-4716" class="content">
      <p><a href="/tv/">View Digital Signage Demonstration</a></p>
<p>Content in TERMINALFOUR can be output in many different ways for many different situations. In the example above we present the same news articles that are show on the website's <a href="/alumni/news/">news page</a>. Having the ability to have multiple different visual outputs of the same underlying content means it is very easy to create something like the above which could be used to show the latest news on campus displays throughout your University.</p>
    </div>
  </dd>  <dd class="accordion-navigation">
    <a href="#item-4715">Emergency Notice</a>
    <div id="item-4715" class="content">
      <p><a href="/notice/">View Emergency Notice</a></p>
<p>Emergency notices can be added to your site to inform students of immergencies in progress. Visit the link above for more details as to how this is implemented.</p>
    </div>
  </dd>  <dd class="accordion-navigation">
    <a href="#item-4714">Events Calendar</a>
    <div id="item-4714" class="content">
      <p><a href="/alumni/events/calendar/">listing</a></p>
<p>Our new PHP Events Calendar&nbsp;can read in events from multiple sources. As well as reading events created by TERMINALFOUR (XML or JSON) the new calendar can read Google Calendar Events as well as iCal feeds.</p>
<p>The different calendar views&nbsp;within it such as the <a href="/alumni/events/calendar/">listing</a>, <a href="/alumni/events/calendar-view/">calendar view</a> and <a href="/alumni/events/calendar/">filter fields</a> are built in a modular way to allow them to be added easily to your own pages as normal content. The HTML and the calendar logic are completely separated so you have full control over how the calendar looks and feels. This allows for as much customisation as neccesary to sit comfortably into your site.</p>
    </div>
  </dd>  <dd class="accordion-navigation">
    <a href="#item-4698">FAQ page</a>
    <div id="item-4698" class="content">
      <p><a href="/life-at-t4u/faq/">View&nbsp;FAQ page</a></p>
<p>The FAQ page is set up using a FAQ widget content type and each FAQ item is added using a different content type (FAQ item). Those items can be anywhere in the Site Structure as the FAQ widget uses the Keyword Navigation object and an element to choose what section the questions are populated from. The accordion interaction is javascript based and uses the default Foundation accordion scripts to achieve the show/hide.</p>
    </div>
  </dd>  <dd class="accordion-navigation">
    <a href="#item-4707">Form Builder</a>
    <div id="item-4707" class="content">
      <p><a href="/alumni/events/create-an-event/">View Form Builder example</a></p>
<p>Form builder is a froms solution built into TERMINALOUR. Forms can be easliy created by dragging and dropping elements into place. You have control over the styling of these forms yourself or you can include our out of the box style for quick user forms.</p>
<p><br>Forms submissions are handled by our secure formbank service so you don't have to worry about your TERMINALFOUR instance going offline.</p>
<p>Form submissions can be sent via email and also brought into TERMINALFOUR as content.</p>
    </div>
  </dd>  <dd class="accordion-navigation">
    <a href="#item-4706">Multiple homepage designs</a>
    <div id="item-4706" class="content">
      <p>View&nbsp;Multiple homepage designs:</p>
<p><a href="/home-1/">View Design 1</a></p>
<p>View Design 2</p>
<p>Two more examples of homepage designs to give options in terms of using the Sample Site&nbsp;as a pop up website.</p>
    </div>
  </dd>  <dd class="accordion-navigation">
    <a href="#item-4703">News Tagging</a>
    <div id="item-4703" class="content">
      <p><a href="/alumni/news/">View&nbsp;News Tagging</a></p>
<p>News tagging is done via javascript. The news content type has an alternate content layout which populates a json file. The tag links on the fulltext of the news content pass a parameter to the URL which the javascript uses to query the json file and return a list of news items that have that tag associated with them.</p>
    </div>
  </dd>  <dd class="accordion-navigation">
    <a href="#item-4702">Programmable Layouts</a>
    <div id="item-4702" class="content">
      <p><a href="/">View&nbsp;Programmable Layouts</a></p>
<p>Programmable layouts are in place on the homepage to cut the news title to a manageable length for the homepage blocks and insert an ellipsis after the characters length. On <a href="/home-2/">homepage layout 2</a> those extra characters are wrapped in span tags to allow for CSS to show or hide that content based on the screen width.</p>
    </div>
  </dd>  <dd class="accordion-navigation">
    <a href="#item-4696">Sitemap</a>
    <div id="item-4696" class="content">
      <p><a href="/sitemap.html">View&nbsp;Sitemap</a></p>
<p>This sitemap is created using the built in Site Manager Site Map navigation object and some javascript added to allow for accordion navigation. It displays all visible sections in the hierarchy.</p>
    </div>
  </dd>  <dd class="accordion-navigation">
    <a href="#item-4704">Social Stream</a>
    <div id="item-4704" class="content">
      <p><a href="/social/">View&nbsp;Social Stream</a></p>
<p>The Social Stream is a jquery based plugin that hooks up to multiple social networks and displays content in a masonry style. Users can filter on a per social network basis. This is a premium plugin and needs a licence for client use. Details of all support and feed options are here&nbsp;<a href="http://codecanyon.net/item/jquery-social-stream/full_screen_preview/2103997?ref=sdeering" target="_blank">http://codecanyon.net/item/jquery-social-stream/full_screen_preview/2103997?ref=sdeering</a>&nbsp;</p>
    </div>
  </dd>  <dd class="accordion-navigation">
    <a href="#item-4705">Staff Directory</a>
    <div id="item-4705" class="content">
      <p><a href="/research/staff-directory/">View&nbsp;Staff Directory</a></p>
<p>Staff Directory works off a similar code base to the Advanced Course Search, but instead of searching course content, it searches Staff content. The PHP code for this search is hugely adaptable and the filters can be run off any element within the selected content type. In this instance the filters include elements such as Last name, Title and Department.</p>
    </div>
  </dd>  <dd class="accordion-navigation">
    <a href="#item-4699">Video Content</a>
    <div id="item-4699" class="content">
      <p><a href="/research/">View&nbsp;Video Content</a></p>
<p>The Sample Site has content types that are built to display either YouTube or Vimeo video content. The TERMINALFOUR user enters in the video URL into the video content type to show their video.</p>
    </div>
  </dd>  <dd class="accordion-navigation">
    <a href="#item-4700">XML/RSS generated from content</a>
    <div id="item-4700" class="content">
      <p><a href="/alumni/news/">View&nbsp;XML/RSS generated from content</a></p>
<p>The XML content is created using a combination of a 'Publish to One' navigation object to collate the information, and a 'Generate File' navigation object to produce the .xml file. In this instance the Publish to One brings back all the news items in the entire site in an alternate content layout text/xml to produce this file.</p>
    </div>
  </dd>
</dl><!-- start of SM footer -->
