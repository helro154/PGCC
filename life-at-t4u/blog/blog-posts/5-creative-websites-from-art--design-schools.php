          <h1>5 creative websites from art &amp; design schools</h1>
          <h2 class="author">Dave O'Dwyer <time datetime="2016-01-18 15:36">18 January, 2016</time></h2>
                                  
          
          
          
<div class="article-image flex-box">
	<img itemprop="image" class="hidden-tablet" src="/media/sample-site/site-assets/images/blog/banner-1.jpg" alt="" />
                                      
          </div>
          
          
                                
          
                                    <p>The university website is an unparalleled resource not only when it comes to informing students about what your institution has to offer but it also goes a long way towards establishing the identity of your university or college.</p>
<p>The above statement is never truer than when it comes to art and design school websites. &nbsp;You know that uncomfortable sense of foreboding and ominous dread you get when the person cutting your hair is sporting a particularly unflattering or crazy new hairstyle? Well it’s kind of the same principle for art-schools; you just expect more from the design of their websites. Just like you expect your hairdresser to have good hair, you expect a school devoted to design to feature a good web design.</p>
<p>The website is at the very core of a university or college’s website and an art or design school has more to prove in terms of its artistic abilities. We have recently spent some time looking at some of the websites offerings from art and design institutions and have picked what we think are five of the best around. We weren’t just looking for eye-catching designs, we also didn’t want to compromise on user experience. All five of the websites feature stunning designs and are easy to navigate.&nbsp;</p>
