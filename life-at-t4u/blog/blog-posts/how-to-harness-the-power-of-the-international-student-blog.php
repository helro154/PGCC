          <h1>How to harness the power of the international student blog</h1>
          <h2 class="author">Louise Ryan <time datetime="2016-01-07 15:40">7 January, 2016</time></h2>
                                  
          
          
          
<div class="article-image flex-box">
	<img itemprop="image" class="hidden-tablet" src="/media/sample-site/site-assets/images/blog/banner-6.jpg" alt="" />
                                      
          </div>
          
          
                                
          
                                    <p>Blogs are now one of the most influential digital resources available to consumers when making a purchasing decision. They have become an integral part of how we research products and services and how we make consumer decisions from what book to read next, where to go on holidays to what brand of laptop to buy.</p>
<p>Blogging has become an extra step in the consumer thought process and a favorable or indeed a less than favorable review can turn a previously steadfast opinion entirely on its head.</p>
<p>So, why exactly are blogs so influential?</p>
<p>The answer’s a simple one: Bloggers tend to be pretty honest in how they review products and services. They talk about what aspects of a certain product they like but they also don’t shy away from what aspects they don’t like. Trust is after all what drives consumers to act.</p>
<p>Blogging is an especially important step when consumers are dealing with a brand that is unfamiliar to them. The same is true when it comes to students who are researching what university or college to attend abroad. While domestic students may recognize your brand, international students are less likely to be familiar with what exactly it is that sets you apart from your competitors.</p>
<p>We’ve recently written a blog post about why&nbsp;<a href="http://www.terminalfour.com/blog/posts/why-mastering-the-art-of-the-student-blog-is-a-must-for-any-university-.html">mastering the art of student blogging is a must for any university</a>&nbsp;and today we want to focus more on international student blogging and why it’s vital for any institution who hopes to attract foreign students.</p>
<p>Universities and college that have an active blog written by and for international students stand a far better chance of appealing to an international student audience. A blog that is written by an international student about their honest experiences of attending your university can be a powerful way of marketing your international studies programme. They can provide insight into cultural differences, tourist attractions, what the support system is like for international students, how to find accommodation and what classes to take.</p>
<p>We actually found it quite challenging to find examples of universities and colleges who are consistently updating their international student blog. However, after trawling the internet we have found 3 universities who we think are doing a pretty great job running their international student blogs:</p>
<ol>
<li>&nbsp;<a href="http://asiapacific.anu.edu.au/news-events/all-stories/yia-blog-all" target="_blank">ANU College of Asia and the Pacific</a></li>
<li><a title="UCC" href="https://uccinternational.wordpress.com/">University College Cork</a>&nbsp;</li>
<li><a href="http://www.aston.ac.uk/international-students/why-choose-aston/meet-aston-international-scholars/" target="_blank">Aston University</a></li>
</ol>
<p>Prove us wrong! Does your university or college have an active international student blog that you would like to share with us? Leave a comment below or email us at marketing [at] terminalfour.com</p>
