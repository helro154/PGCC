          <h1>What I took away from this year&rsquo;s Noel-Levitz conference</h1>
          <h2 class="author">S&eacute;an Walsh <time datetime="2016-01-19 15:40">19 January, 2016</time></h2>
                                  
          
          
          
<div class="article-image flex-box">
	<img itemprop="image" class="hidden-tablet" src="/media/sample-site/site-assets/images/blog/banner-2.jpg" alt="" />
                                      
          </div>
          
          
                                
          
                                    <h2>Measure, measure and measure</h2>
<p>‘Do you know if your marketing is performing’? Marketing often struggles to prove its value. People know it helps and they know they need to ‘do it’ but because it is perceived as a spending function it has sometimes been unable to prove its direct impact on organizational goals. This is why marketers love digital marketing. You can measure visits, conversions, hits, downloads, requests and a million other factors. It’s the reason why every marketing person has bought the t-shirt saying ‘If you can’t measure it you can’t manage it’ or the latest iteration’ if you can’t measure it you can’t improve it’.</p>
<p><br>Measuring is vital. You should be able to track to conversion, you should have tracking codes on every web page, you should be A/B testing, you should be measuring and analyzing key word impact. The overwhelming message from the conference was measure, measure, measure. But a word to the wise; just because you can’t measure something doesn’t mean it doesn’t have an impact. This relates back to the offline debate. You don’t know the impact of a person seeing your billboard driving through your city when they were on vacation with their parents last year. You don’t know that that billboard encouraged them to go to your website when they got home and request more information. You can’t throw the baby out with the bathwater because offline is difficult to track and measure; its impact just may be very real.</p>
<h2>Be part of the conversation</h2>
<p>‘Why do we not connect storytelling to strategy’? One of the big themes of the conference was engagement; connecting your audiences and have meaningful conversations with them. Universities and colleges need to communicate with purpose and intent. They need to be conscious of the organizational goals but understand how storytelling, in all its forms, can deliver on them. A simple example is around student recruitment. Are you just disseminating information about the university or a specific course? Or are you, for example, letting a current student or recent graduate tell their story (perhaps through video)about how they’ve achieved something significant to them by attending your university or graduating with ‘that degree’. This is powerful, impactful and memorable. ‘A picture is worth a thousand words. One minute of video is worth 1.8 million words’</p>
<h2>The Keynote: From Homeless to Harvard</h2>
<p>It’s very rare that a key note at a conference can render an entire conference room of 1,500+ plus silent for an hour but that’s exactly what happened when Liz Murray took to the stage. If you didn’t know her story to be true you’d never believe it could be. From living with drug addicted parents, through abject poverty, through her parents’ death and homelessness to becoming a Harvard graduate, her self-told story (without self-pity or judgement) was inspirational and transformational. Her belief in ‘What if…?’ will change the way you think about your own life and situation. ‘What if’ the 150th door you open (after 149th slap you in the face) is the one that changes your life. Hers is a story of how education gave her a future and helped her be at peace with her past. It’s too hard to convey her message in a short blog but I bought her book, ‘Breaking Night: A memoir of forgiveness, survival and my journey from homeless to Harvard’- I’d recommend you do too.</p>
