<div class="general">
  
  
  <div class="content"><p>The student services at the University of TERMINALFOUR are focused on helping their students have the best possible university experience as well as helping them achieve their goals. All students need guidance and assistance every once in a while which is why our doors are always open.</p>
<p>Our student services are there to offer support and advice to students should they need it. The student services office is located in our main campus building and is open during normal campus hours.</p>
<p>Our aim is to work closely with students, faculty, and staff. Student Services aims to provide services, activities, and resources that support the TERMINALFOUR University’s educational enterprise and support each student to learn, grow, and develop during their time here.</p>
<p>There are a number of different resources available to all TERMINALFOUR students and all our advisers are friendly, approachable and happy to help!</p>
<p>We have a dedicated international office to help coordinate students who wish to attend or are currently enrolled in the university. We offer advice to students about tuition, accommodation, registration, how to connect with your peers and how to integrate into our community. Our international office is dedicated to making the transition for students travelling from other countries as smooth as possible.</p>
<p>If you have any queries about any of our student services or just want a friendly voice to chat to, we’re always here- call in, fill out an online enquiry form or contact us on our dedicated helpline.&nbsp;</p></div>
