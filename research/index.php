<div class="general">
  
  <p class="intro">TERMINALFOUR University offers an extraordinary range and diversity of research opportunities that students and faculty enjoy. The university provides unparalleled opportunities for learning and research.</p>
  <div class="content"><p>We are constantly looking for ways to apply our research to help make new breakthroughs in technology.</p>
<p>This centre of research excellence shows our commitment to excellence and being at the forefront of new knowledge.</p>
<p>Across the university, we have teams of expert researchers working on projects and trials.</p>
<p>Our research scholars engage in research that extends beyond the classroom and around the globe. We pride ourselves on tackling issues that are of huge benefit.</p>
<p>We are committed to an intellectual environment that is conducive to the discovery and communication of new knowledge.</p>
<p>Generating new knowledge for the benefit of present and future generations, TERMINALFOUR University research has had an impact in tech around the globe in areas such as personalization, variance testing, responsive design and accessibility among other areas.&nbsp;</p>
<p>&nbsp;</p></div>
</div>
