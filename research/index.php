    <h2>Student Research</h2>
<p>At TERMINALFOUR University, we have a firm believe that some of the biggest and best ideas come from fresh viewpoints.</p>
<p>Our university is driven by a united reason: an insatiable intellectual curiosity.</p>
<p><img src="/media/sample-site/content-assets/global-content-assets/images/news-images/43H-150.jpg" alt="Peoples legs" style="width : 150px; height : 150px;     " /></p>
<p>TERMINALFOUR researchers make advancements in higher education technology.</p>
  </div>
  <div class="small-12 large-6 columns">
  	<p>&nbsp;Students conduct and contribute to research with opportunities and assistance from the following resources:</p>
<ul>
<li>TERMINALFOUR University Careers department: advice, training, networking opportunities, research and internship opportunities</li>
<li>The Research and development department: research developments and internship opportunities</li>
<li>Student support, a searchable online database of funding sources, offers support to students when and where needed</li>
</ul>
<p>Our researchers&nbsp;secured more than $200 million in research awards during fiscal year 2013.</p>
